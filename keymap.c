// ============================================================
//  Anim 1 — Bouncing Ball (no border)
// ============================================================
static int16_t ball_x = 20, ball_y = 8;
static int8_t  ball_vx = 2, ball_vy = 1;
#define TRAIL_LEN 20
static uint8_t trail_x[TRAIL_LEN];
static uint8_t trail_y[TRAIL_LEN];
static uint8_t trail_head = 0;
static bool    trail_full = false;

static void ball_reset(void) {
    ball_x = 20; ball_y = 8;
    ball_vx = 2; ball_vy = 1;
    trail_head = 0; trail_full = false;
}

static void anim_bouncing_ball(void) {
    oled_clear();
    uint8_t bx = (uint8_t)ball_x;
    uint8_t by = (uint8_t)ball_y;

    trail_x[trail_head] = bx + 3;
    trail_y[trail_head] = by + 3;
    trail_head = (trail_head + 1) % TRAIL_LEN;
    if (trail_head == 0) trail_full = true;

    ball_x += ball_vx;
    ball_y += ball_vy;
    if (ball_x <= 0 || ball_x >= 120) { ball_vx = -ball_vx; ball_x += ball_vx; }
    if (ball_y <= 0 || ball_y >= 24)  { ball_vy = -ball_vy; ball_y += ball_vy; }

    uint8_t count = trail_full ? TRAIL_LEN : trail_head;
    for (uint8_t i = 0; i < count; i++) {
        uint8_t idx = (trail_head - 1 - i + TRAIL_LEN) % TRAIL_LEN;
        uint8_t tx = trail_x[idx], ty = trail_y[idx];
        if (i < 3) {
            for (int8_t dx = -1; dx <= 2; dx++)
                for (int8_t dy = -1; dy <= 2; dy++)
                    if (tx+dx < 128 && ty+dy < 32)
                        oled_write_pixel(tx+dx, ty+dy, true);
        } else if (i < 8) {
            oled_write_pixel(tx, ty, true);
            oled_write_pixel(tx+1, ty, true);
            oled_write_pixel(tx, ty+1, true);
            oled_write_pixel(tx+1, ty+1, true);
        } else {
            oled_write_pixel(tx, ty, true);
        }
    }

    bx = (uint8_t)ball_x; by = (uint8_t)ball_y;
    for (int8_t dx = 2; dx <= 5; dx++) oled_write_pixel(bx+dx, by, true);
    for (int8_t dx = 1; dx <= 6; dx++) oled_write_pixel(bx+dx, by+1, true);
    for (int8_t dy = 2; dy <= 5; dy++)
        for (int8_t dx = 0; dx <= 7; dx++)
            oled_write_pixel(bx+dx, by+dy, true);
    for (int8_t dx = 1; dx <= 6; dx++) oled_write_pixel(bx+dx, by+6, true);
    for (int8_t dx = 2; dx <= 5; dx++) oled_write_pixel(bx+dx, by+7, true);
}

