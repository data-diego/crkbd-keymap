static void render_animation(void) {
    if (anim_id != last_anim_id) {
        oled_clear();
        ball_reset();
        wave_phase = 0;
        nyan_frame = 0;
        bongo_frame = 0;
        ocean_phase = 0;
        crab_frame = 0;
        palm_frame = 0;
        last_anim_id = anim_id;
    }
    switch (anim_id) {
        case 0: anim_bongo_cat();      break;
        case 1: anim_bouncing_ball();  break;
        case 2: anim_sine_wave();      break;
        case 3: anim_gru();            break;
        case 4: anim_diego_cat();      break;
        case 5: anim_nyan_cat();       break;
        case 6: anim_ocean_dream();    break;
        case 7: anim_crab();           break;
        case 8: anim_palm_tree();      break;
    }
}

bool oled_task_user(void) {
    uint16_t ms = (anim_id == 0 || anim_id >= 6) ? ANIM_FRAME_SLOW : ANIM_FRAME_FAST;
    if (timer_elapsed(anim_timer) > ms) {
        anim_timer = timer_read();
        render_animation();
    }
    return false;
}

