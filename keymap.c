// ============================================================
//  Bitmap helper — draws via oled_write_pixel so OLED rotation
//  on the slave half is handled correctly
// ============================================================
static void draw_icon_P(const char *data, uint8_t x_off, uint8_t y_off,
                        uint8_t w, uint8_t h) {
    uint8_t pages = (h + 7) >> 3;
    for (uint8_t page = 0; page < pages; page++) {
        uint8_t y_base = y_off + (page << 3);
        for (uint8_t col = 0; col < w; col++) {
            uint8_t byte = pgm_read_byte(&data[page * w + col]);
            if (byte == 0) continue;
            uint8_t px = x_off + col;
            if (px >= 128) continue;
            for (uint8_t bit = 0; bit < 8; bit++) {
                if (byte & (1 << bit)) {
                    uint8_t py = y_base + bit;
                    if (py < 32)
                        oled_write_pixel(px, py, true);
                }
            }
        }
    }
}

