bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        gru_frame++;
        last_keypress_time = timer_read();
        bongo_tap_hand ^= 1;

        uint8_t mods = get_mods();
        if ((mods & MOD_BIT(KC_LSFT)) && (mods & MOD_BIT(KC_RSFT))) {
            switch (keycode) {
                case KC_A: anim_id = 0; return false;
                case KC_S: anim_id = 1; return false;
                case KC_D: anim_id = 2; return false;
                case KC_F: anim_id = 3; return false;
                case KC_G: anim_id = 4; return false;
                case KC_H: anim_id = 5; return false;
                case KC_J: anim_id = 6; return false;
                case KC_K: anim_id = 7; return false;
                case KC_L: anim_id = 8; return false;
                case KC_Z: remote_anim = 0; return false;
                case KC_X: remote_anim = 1; return false;
                case KC_C: remote_anim = 2; return false;
                case KC_V: remote_anim = 3; return false;
                case KC_B: remote_anim = 4; return false;
                case KC_N: remote_anim = 5; return false;
                case KC_M: remote_anim = 6; return false;
                case KC_COMM: remote_anim = 7; return false;
                case KC_DOT: remote_anim = 8; return false;
            }
        }
    }
    return true;
}

